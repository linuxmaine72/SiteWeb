                    <p class="titre1" align="center">Bienvenue sur le site de
                      l'association linuX Maine</p>
                    <p class="normal">L'association Linux Maine est une
                      association loi 1901 qui a pour but de promouvoir le
                      système d'exploitation Linux ainsi que ses applicatifs.</p>
                    <p class="normal">Son rôle est de fédérer les utilisateurs
                      de la région du Mans et plus globalement de la Sarthe par
                      des actions de formations, de sensibilisation, de
                      vulgarisation et d'organisation de manifestations.</p>
                    <p class="normal">Elle se veut vivante et ouverte à tous
                      respectant la philosophie open source de GNU Linux.</p>
